/*=========================================================================================
REQUIRED MODULES
=========================================================================================*/

const mongoose = require("mongoose");

/*=========================================================================================
VARIABLES
=========================================================================================*/

const Schema = mongoose.Schema;

/*=========================================================================================
CREATE PROJECT MODEL
=========================================================================================*/

const ProjectSchema = new Schema({
  account: {
    type: Schema.Types.ObjectId,
    required: true
  },
  name: {
    type: String,
    required: true
  },
  thumbnail: {
    type: Schema.Types.ObjectId
  },
  bookmark: {
    type: Boolean,
    default: false
  },
  makes: {
    type: [Schema.Types.ObjectId],
    default: []
  },
  date: {
    creation: {
      type: String,
      required: true
    },
    modified: {
      type: String,
      required: true
    }
  },
  note: {
    type: String,
    default: ""
  }
});

/*=========================================================================================
STATIC
=========================================================================================*/

/*=========================================================================================
METHOD
=========================================================================================*/

/*=========================================================================================
EXPORT ACCOUNT MODEL
=========================================================================================*/

module.exports = Project = mongoose.model("projects", ProjectSchema);

/*=========================================================================================
END
=========================================================================================*/
