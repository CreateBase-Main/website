/*=========================================================================================
REQUIRED MODULES
=========================================================================================*/

const mongoose = require("mongoose");

/*=========================================================================================
VARIABLES
=========================================================================================*/

const Schema = mongoose.Schema;

/*=========================================================================================
CREATE ORDER MODEL
=========================================================================================*/

const OrderSchema = new Schema({
  accountId: {
    type: Schema.Types.ObjectId
  },
  makes: {
    type: [Schema.Types.ObjectId]
  },
  items: {
    type: [Schema.Types.ObjectId]
  },
  discounts: {
    type: [Schema.Types.ObjectId]
  },
  manufacturingSpeed: {
    type: String
  },
  status: {
    type: String
  }
});

/*=========================================================================================
EXPORT ORDER MODEL
=========================================================================================*/

module.exports = Order = mongoose.model("orders", OrderSchema);

/*=========================================================================================
END
=========================================================================================*/
