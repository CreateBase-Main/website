/*=========================================================================================
REQUIRED MODULES
=========================================================================================*/

const mongoose = require("mongoose");

/*=========================================================================================
VARIABLES
=========================================================================================*/

const Schema = mongoose.Schema;

/*=========================================================================================
CREATE ITEM MODEL
=========================================================================================*/

const ItemSchema = new Schema({
  accountId: {
    type: Schema.Types.ObjectId
  },
  status: {
    type: String
  }
});

/*=========================================================================================
EXPORT ITEM MODEL
=========================================================================================*/

module.exports = Item = mongoose.model("items", ItemSchema);

/*=========================================================================================
END
=========================================================================================*/
