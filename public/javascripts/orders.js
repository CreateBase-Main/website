let orders = {
  fetchOrder: undefined
}

/* ========================================================================================
FUNCTIONS
======================================================================================== */

// @func  orders.fetchOrder
// @desc  
orders.fetchOrder = () => {
  return new Promise(async (resolve, reject) => {
    let data;
    try {
      data = (await axios.get("/order/fetch-orders"))["data"];
    } catch (error) {
      return reject(error);
    }
    return resolve(data);
  })
}

